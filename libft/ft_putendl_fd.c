#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	unsigned int i;

	i = 0;
	if (fd < 0)
		return ;
	if (!s)
	{
		ft_putchar_fd('\n', fd);
		return ;
	}
	while (s[i] != '\0')
	{
		ft_putchar_fd(s[i], fd);
		i++;
	}
	ft_putchar_fd('\n', fd);
}
